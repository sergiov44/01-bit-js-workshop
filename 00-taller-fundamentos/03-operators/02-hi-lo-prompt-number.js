// 02 Prompted Higher or Lower Number

let number1 = 1;
let number2 = parseFloat(prompt("Type a number: "));

if (number1 > number2) {
    console.log("My number is higher than yours");
} else if (number1 < number2) {
    console.log("Your number is higher than mine");
} else if (number1 = number2) {
    console.log("Our numbers are equal");
} else {
    console.log("Wut?");
};